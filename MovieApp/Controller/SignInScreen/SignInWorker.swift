import UIKit
import Firebase

class SignInWorker
{
    static let shared = SignInWorker()

    func userSignIn(email: String, password: String, completion: @escaping(AuthDataResult?, Error?) -> Void) {
        Auth.auth().signIn(withEmail: email, password: password, completion: completion)
    }
}
